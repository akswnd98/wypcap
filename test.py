packet = (ctypes.c_ubyte * 42)()

packet[0:6] = 0x04, 0x7C, 0x16, 0x45, 0x32, 0xF8
packet[6:12] = 0x04, 0x7C, 0x16, 0x45, 0x32, 0xF8
packet[12:14] = 0x8, 0x6
packet[14:16] = 0x00, 0x01
packet[16:18] = 0x8, 0x0
packet[18] = 0x6
packet[19] = 0x4
packet[20:22] = 0x0, 0x1
packet[22:28] = 0x90, 0x9f, 0x33, 0xeb, 0x3b, 0xa5
packet[28:32] = 192, 168, 45, 169
packet[32:38] = 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
packet[38:42] = 192, 168, 45, 169

devs = wypcap.findalldevs()
print(devs)
dev = devs[0][0]
print(dev)
pcap_inst = wypcap.pcap(dev)
print(pcap_inst.sendpacket(bytes(packet)))
